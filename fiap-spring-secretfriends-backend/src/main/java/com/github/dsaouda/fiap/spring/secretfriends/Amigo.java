package com.github.dsaouda.fiap.spring.secretfriends;

public class Amigo {
	
	private String nome;
	private String a;
	private String b;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	
	
	
}
